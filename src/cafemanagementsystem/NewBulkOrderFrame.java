    private void sPNameTxtBoxKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_sPNameTxtBoxKeyTyped
        try {

            Product pdct = new Product();
            ResultSet rs = pdct.viewProduct("*", "name like ? and availability = ?", "%" + sPNameTxtBox.getText() + "%", 1);
            DefaultTableModel df = (DefaultTableModel) productInfoTable.getModel();
            int rc = df.getRowCount();
            for (int i = 0; i < rc; i++) {
                df.removeRow(0);
            }

            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("product_id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("price"));
                df.addRow(v);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_sPNameTxtBoxKeyTyped

