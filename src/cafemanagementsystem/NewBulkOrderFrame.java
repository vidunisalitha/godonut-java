    private void addToCartBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addToCartBtnActionPerformed
        if (pNameTxtBox.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Please select a product");
        } else if (qtySpinner.getValue() == 0) {
            JOptionPane.showMessageDialog(null, "Quantity cannot be 0");
        } else {
            DefaultTableModel df = (DefaultTableModel) cartTable.getModel();
            int rowCount = df.getRowCount();
            boolean duplicateFound = false;

            // Iterate over all rows to find if the product is already in the cart
            for (int i = 0; i < rowCount; i++) {
                Object cellValue = df.getValueAt(i, 0);
                if (cellValue != null && pNameTxtBox.getText().equals(cellValue.toString())) {
                    duplicateFound = true;
                    break;
                }
            }

            if (duplicateFound) {
                JOptionPane.showMessageDialog(null, "Already in the cart");
            } else {
                double price = Double.parseDouble(priceTxtBox.getText());
                int quantity = qtySpinner.getValue();
                double total = price * quantity;

                Object[] data = {pNameTxtBox.getText(), priceTxtBox.getText(), quantity, total};
                df.addRow(data);
                //pList.add(pid);
                setTotal();
            }
        }

    }//GEN-LAST:event_addToCartBtnActionPerformed

