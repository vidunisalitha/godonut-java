    private void saveSupplierBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveSupplierBtnActionPerformed
        String fname = fNameTxtBox.getText();
        String lname = lNameTxtBox.getText();
        String addres1 = addrLineTxtBox01.getText();
        String addres2 = addrLineTxtBox02.getText();
        String email = suppEmailTxtBox.getText();
        String ctn1 = suppContactTxtBox01.getText();
        String ctn2 = suppContactTxtBox02.getText();

        if (fname.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Enter First Name");
            fNameTxtBox.grabFocus();
        } else if (lname.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Enter Last Name");
            lNameTxtBox.grabFocus();
        } else if (addres1.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Enter Address line 01");
            addrLineTxtBox01.grabFocus();
        } else if (addres2.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Enter Address line 02");
            addrLineTxtBox02.grabFocus();
        } else if (email.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Enter Email");
            suppEmailTxtBox.grabFocus();
        } else if (ctn1.trim().isEmpty() || ctn2.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Enter Email");
            suppContactTxtBox01.grabFocus();
            suppContactTxtBox02.grabFocus();
        } else {

            Supplier supplier = new Supplier(fname, lname, addres1, addres2, email, ctn1);
            supplier.addsupplier(ctn2);
            loadSuppliers();

        }
    }//GEN-LAST:event_saveSupplierBtnActionPerformed

    private void supplierTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_supplierTableMouseClicked
        DefaultTableModel df = (DefaultTableModel) supplierTable.getModel();

        supid = Integer.parseInt(df.getValueAt(supplierTable.getSelectedRow(), 0).toString());
        String supName = df.getValueAt(supplierTable.getSelectedRow(), 1).toString();
        String nameSplit[] = supName.split(" ");
        fNameTxtBox.setText(nameSplit[0]);
        lNameTxtBox.setText(nameSplit[1]);

        String supAddr = df.getValueAt(supplierTable.getSelectedRow(), 2).toString();
        String addrSplit[] = supAddr.split(",");
        addrLineTxtBox01.setText(addrSplit[0]);
        addrLineTxtBox02.setText(addrSplit[1]);

        suppEmailTxtBox.setText(df.getValueAt(supplierTable.getSelectedRow(), 3).toString());
        suppContactTxtBox01.setText(df.getValueAt(supplierTable.getSelectedRow(), 4).toString());
        suppContactTxtBox02.setText(df.getValueAt(supplierTable.getSelectedRow(), 5).toString());
    }//GEN-LAST:event_supplierTableMouseClicked

