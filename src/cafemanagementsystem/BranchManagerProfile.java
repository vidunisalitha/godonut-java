    public void loadRequests() {

        try {

            Request req = new Request();
            User user = new User();
            ResultSet rs = req.viewRequest("*", "status = ?", "BM");

            DefaultTableModel df = (DefaultTableModel) hrInfoTable.getModel();
            int rc = df.getRowCount();
            for (int i = 0; i < rc; i++) {
                df.removeRow(0);
            }

            while (rs.next()) {

                Vector v = new Vector();
                String uId = rs.getString("user_id");
                v.add(uId);

                ResultSet rs2 = user.viewUser("fname", "user_id = ?", uId);
                String fName = "";
                if (rs2.next()) {
                    fName = rs2.getString("fname");
                }

                v.add(fName);
                v.add(rs.getString("description"));
                v.add(rs.getString("status"));
                v.add(rs.getString("req_id"));
                df.addRow(v);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

