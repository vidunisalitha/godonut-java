    private void approveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_approveBtnActionPerformed
        DefaultTableModel df = (DefaultTableModel) hrInfoTable.getModel();

        if (hrInfoTable.getSelectedRowCount() == 0) {
            JOptionPane.showMessageDialog(null, "Select at least one Request to approve");
        } else {
            JFrame f = new JFrame();
            if (JOptionPane.showConfirmDialog(f, "Do you want to perform this action") == JOptionPane.YES_NO_OPTION) {
                int[] selectedRows = hrInfoTable.getSelectedRows();

                Request req = new Request();

                for (int row : selectedRows) {
                    int modelRow = hrInfoTable.convertRowIndexToModel(row);
                    req.setReqId(Integer.parseInt(df.getValueAt(modelRow, 4).toString()));
                    req.setStatus("Approved");
                    req.approveRequest();
                }
                loadRequests();
            }
        }
    }//GEN-LAST:event_approveBtnActionPerformed

