    private void loadTasks() {

        try {

            Task task = new Task();
            ResultSet rs = task.viewTask("*", "Inner join User as u on t.user_id = u.user_id", " role = ?", "Employee");

            DefaultTableModel df = (DefaultTableModel) taskTable.getModel();
            int rc = df.getRowCount();
            for (int i = 0; i < rc; i++) {
                df.removeRow(0);
            }

            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("t.description"));
                v.add(rs.getString("t.user_id"));
                v.add(rs.getString("t.due_date"));
                v.add(rs.getString("t.status"));
                v.add(rs.getString("t.task_id"));
                df.addRow(v);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

