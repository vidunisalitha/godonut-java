    private void deleteBtntActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtntActionPerformed
        DefaultTableModel df = (DefaultTableModel) taskTable.getModel();
        if ((taskTable.getSelectedRowCount() == 0) || (taskTable.getSelectedRowCount() > 1)) {
            JOptionPane.showMessageDialog(null, "Please select a task to Delete");
        } else {

            JFrame f = new JFrame();
            if (JOptionPane.showConfirmDialog(f, "Do you want to delete this task?") == JOptionPane.YES_NO_OPTION) {

                int taskId = Integer.parseInt(df.getValueAt(taskTable.getSelectedRow(), 4).toString());

                Task task = new Task();
                task.setTaskId(taskId);
                task.removeTask();
                loadTasks();

            }

        }
    }//GEN-LAST:event_deleteBtntActionPerformed

    private void taskTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_taskTableMouseClicked
        DefaultTableModel df = (DefaultTableModel) taskTable.getModel();

        taskDescriptionTxtBox.setText(df.getValueAt(taskTable.getSelectedRow(), 0).toString());

        String tableDate = df.getValueAt(taskTable.getSelectedRow(), 2).toString();
        DateTimeFormatter formater = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        LocalDate lDate = LocalDate.parse(tableDate, formater);
        Date date = Date.from(lDate.atStartOfDay(ZoneId.systemDefault()).toInstant());
        dueDateChooser.setDate(date);
    }//GEN-LAST:event_taskTableMouseClicked

    private void empTaskTxtBoxKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_empTaskTxtBoxKeyTyped
        try {

            Task task = new Task();
            ResultSet rs = task.viewTask("*", "Inner join User as u on t.user_id = u.user_id", "role = ? and description Like ?", "Employee", "%" + empTaskTxtBox.getText() + "%");

            DefaultTableModel df = (DefaultTableModel) taskTable.getModel();
            int rc = df.getRowCount();
            for (int i = 0; i < rc; i++) {
                df.removeRow(0);
            }

            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("t.description"));
                v.add(rs.getString("t.user_id"));
                v.add(rs.getString("t.due_date"));
                v.add(rs.getString("t.status"));
                v.add(rs.getString("t.task_id"));
                df.addRow(v);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_empTaskTxtBoxKeyTyped

