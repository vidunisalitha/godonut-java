    private void serachEmpBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_serachEmpBtnActionPerformed
        if (empIdTxtBox.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Enter User Id");
            empIdTxtBox.grabFocus();
        } else {
            try {
                User user = new User();
                ResultSet rs = user.viewUser("*", "user_id = ? and role = ? or  role = ?", empIdTxtBox.getText(), "Employee", "chef");

                DefaultTableModel df = (DefaultTableModel) empDetailsTable.getModel();
                int rc = df.getRowCount();
                for (int i = 0; i < rc; i++) {
                    df.removeRow(0);
                }

                if (!rs.next()) {
                    JOptionPane.showMessageDialog(null, "User id does not exist or User has other role");
                    loadUsers();
                } else {
                    do {
                        Vector v = new Vector();
                        String userId = rs.getString("user_id");
                        v.add(userId);
                        v.add(rs.getString("fname"));
                        v.add(rs.getString("role"));
                        v.add(rs.getString("email"));
                        df.addRow(v);
                    } while (rs.next());
                }
                rs.close();

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_serachEmpBtnActionPerformed

