    private void updateMenuBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateMenuBtnActionPerformed
        DefaultTableModel df = (DefaultTableModel) empDetailsTable.getModel();
        DefaultTableModel df2 = (DefaultTableModel) taskTable.getModel();
        if (empDetailsTable.getSelectedRowCount() != 1) {
            JOptionPane.showMessageDialog(null, "Please select exactly one employee.");
        } else if (taskTable.getSelectedRowCount() != 1) {
            JOptionPane.showMessageDialog(null, "Please select exactly one task.");
        } else {

            if (empNameTxtBox.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(null, "Please select an Employee");
                empNameTxtBox.grabFocus();
            } else if (taskDescriptionTxtBox.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(null, "Enter Task Description");
                taskDescriptionTxtBox.grabFocus();
            } else if (dueDateChooser.getDate() == null) {
                JOptionPane.showMessageDialog(null, "Selec a due date");
                taskDescriptionTxtBox.grabFocus();
            } else {

                if (df2.getValueAt(taskTable.getSelectedRow(), 3).toString().equalsIgnoreCase("Pending")) {
                    DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy/MM/dd");
                    Date selectedDate = dueDateChooser.getDate();
                    LocalDate localDate = selectedDate.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();

                    Task task = new Task();
                    task.setDescription(taskDescriptionTxtBox.getText());
                    task.setDueDate(localDate);
                    task.setStatus(df2.getValueAt(taskTable.getSelectedRow(), 3).toString());
                    task.setTaskId(Integer.parseInt(df2.getValueAt(taskTable.getSelectedRow(), 4).toString()));
                    task.updateTask(uid);
                    loadTasks();
                } else {
                    JOptionPane.showMessageDialog(null, "You cant edit a completed task");
                }

            }

        }
    }//GEN-LAST:event_updateMenuBtnActionPerformed

