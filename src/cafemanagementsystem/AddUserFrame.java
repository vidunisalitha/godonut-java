    public void IncrementUserId() {

        userId = "USER-1000";
        try {
            String getID = "Select * from user";
            ResultSet rs = MyConn.search(getID);
            while (rs.next()) {

                String id = rs.getString("user_id");
                String splitId[] = id.split("-");
                int lastVal = Integer.parseInt(splitId[1]);
                lastVal++;
                userId = "USER-" + lastVal;

            }

            userIdTxtBox.setText(userId);
            userIdTxtBox.disable();
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

