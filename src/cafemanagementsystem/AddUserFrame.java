    private void updateUserBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateUserBtnActionPerformed
        String userID = userIdTxtBox.getText();
        String fName = fNameTxtBox1.getText();
        String lName = lNameTxtBox.getText();
        String nic = nicTxtBox.getText();
        String addr1 = addrTxtBox1.getText();
        String addr2 = addrTxtBox2.getText();
        String contact1 = contactTxtBox01.getText();
        String contact2 = contactTxtBox02.getText();
        String email = emailTxtBox1.getText();
        double salary = Double.parseDouble(salaryTxtBox.getText());
        String role = userRoleTxtBox.getText();

        if (userID.trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Enter User Id");
            userIdTxtBox.grabFocus();
        } else if (fName.trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Enter First Name");
            fNameTxtBox1.grabFocus();
        } else if (lName.trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Enter Last Name");
            lNameTxtBox.grabFocus();
        } else if (nic.trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Enter NIC");
            nicTxtBox.grabFocus();
        } else if (addr1.trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Enter Address Line 01");
            addrTxtBox1.grabFocus();
        } else if (addr2.trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Enter Address Line 02");
            addrTxtBox2.grabFocus();
        } else if (contact1.trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Enter Contact 1");
            contactTxtBox01.grabFocus();
        } else if (contact2.trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Enter Contact 2");
            contactTxtBox02.grabFocus();
        } else if (email.trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Enter Email");
            emailTxtBox1.grabFocus();
        } else if (salaryTxtBox.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Enter Salary");
            salaryTxtBox.grabFocus();
        } else if (role.trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Enter User Role");
            salaryTxtBox.grabFocus();
        } else {

            JFrame f = new JFrame();
            if (JOptionPane.showConfirmDialog(f, "Do you Want to update this user?") == JOptionPane.YES_NO_OPTION) {

                User user = new User();
                user.setId(userID);
                user.setFName(fName);
                user.setLName(lName);
                user.setNic(nic);
                user.setAddress1(addr1);
                user.setAddress2(addr2);
                user.setTele(contact1);
                user.setEmail(email);
                user.setSalary(salary);
                user.setRole(role);

                user.updateUser("admin", contact2);
                IncrementUserId();

            } else {
                JOptionPane.showMessageDialog(null, "Process Canceled");
            }

        }
    }//GEN-LAST:event_updateUserBtnActionPerformed

