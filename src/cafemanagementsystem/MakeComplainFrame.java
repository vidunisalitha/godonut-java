    private void deleteCmptBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteCmptBtnActionPerformed
        DefaultTableModel df = (DefaultTableModel) complaintTable.getModel();

        if (complaintTable.getSelectedRowCount() == 0) {
            JOptionPane.showMessageDialog(null, "Please select a complaint to delete");
        } else {
            
            Complaint cmp = new Complaint();
            cmp.setComId(Integer.parseInt(df.getValueAt(complaintTable.getSelectedRow(), 3).toString()));
            cmp.removeComplaint();
            loadComplaints();
            
        }
    }//GEN-LAST:event_deleteCmptBtnActionPerformed

