    private void makeCmptBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_makeCmptBtnActionPerformed
        if (problemTxtBox.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Enter your problem");
            problemTxtBox.grabFocus();
        } else {

            Complaint cmp = new Complaint();

            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy/MM/dd");

            cmp.setDescription(problemTxtBox.getText());
            cmp.setMDate(LocalDate.parse(dateLable.getText(), formatter));
            cmp.setStatus("Pending");

            cmp.makeComplaint();
            loadComplaints();

        }
    }//GEN-LAST:event_makeCmptBtnActionPerformed

