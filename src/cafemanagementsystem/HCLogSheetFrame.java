    public void LoadLogSheet() {

        try {

            Logsheet sheet = new Logsheet();
            ResultSet rs = sheet.viewLog("*", "user_id = ?", uIDLable.getText());

            DefaultTableModel df = (DefaultTableModel) logDetails.getModel();
            int rc = df.getRowCount();
            for (int i = 0; i < rc; i++) {
                df.removeRow(0);
            }

            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("type"));
                v.add(rs.getString("description"));
                v.add(rs.getString("made_date"));
                v.add(rs.getString("sheet_id"));
                df.addRow(v);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

