    private void saveLogBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveLogBtnActionPerformed
        if (logDescriptionTxtBox.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Enter Log Descriptions");
            logDescriptionTxtBox.grabFocus();
        } else {

            java.sql.Date date = java.sql.Date.valueOf(LocalDate.now());

            Logsheet sheet = new Logsheet(logTypeCmbBox.getSelectedItem().toString(), date, logDescriptionTxtBox.getText());
            sheet.addLog(uIDLable.getText());
            LoadLogSheet();

        }
    }//GEN-LAST:event_saveLogBtnActionPerformed

