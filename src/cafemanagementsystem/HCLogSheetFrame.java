    private void updateLogBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateLogBtnActionPerformed
        if (lid == 0) {
            JOptionPane.showMessageDialog(null, "You Cannot Perform this action");
        } else {

            if (logDescriptionTxtBox.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Enter Log Descriptions");
                logDescriptionTxtBox.grabFocus();
            } else {

                Logsheet sheet = new Logsheet();
                sheet.setType(logTypeCmbBox.getSelectedItem().toString());
                sheet.setDescription(logDescriptionTxtBox.getText());
                sheet.setSheetID(lid);
                sheet.updateLog();
                LoadLogSheet();

            }

        }
    }//GEN-LAST:event_updateLogBtnActionPerformed

