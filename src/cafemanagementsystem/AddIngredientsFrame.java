    public void loadIngreProducts() {

        try {

            String search = "Select * from ((item_product as ip Inner join inventory_item as ii on ip.item_id = ii.item_id) Inner join product as p on p.product_id = ip.product_id)";
            ResultSet rs = MyConn.search(search);

            DefaultTableModel df = (DefaultTableModel) productDetailsTable2.getModel();
            int rc = df.getRowCount();
            for (int i = 0; i < rc; i++) {
                df.removeRow(0);
            }

            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("p.name"));
                v.add(rs.getString("ii.name"));
                df.addRow(v);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

