    public void loadItem() {

        try {

            InventoryItem item = new InventoryItem();
            ResultSet rs = item.viewInvenory("*", "type = ?", "Ingredient");

            DefaultTableModel df = (DefaultTableModel) ingredientsTable.getModel();
            int rc = df.getRowCount();
            for (int i = 0; i < rc; i++) {
                df.removeRow(0);
            }

            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("item_id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("price"));
                v.add(rs.getString("quantity"));
                df.addRow(v);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

