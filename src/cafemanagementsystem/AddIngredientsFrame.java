    private void pNameTxtBoxKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_pNameTxtBoxKeyTyped
        try {

            Product product = new Product();
            ResultSet rs = product.viewProduct("*", "name like ?", "%" + pNameTxtBox.getText() + "%");

            DefaultTableModel df = (DefaultTableModel) productDetailsTable1.getModel();
            int rc = df.getRowCount();
            for (int i = 0; i < rc; i++) {
                df.removeRow(0);
            }

            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("product_id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("category"));
                v.add(rs.getString("description"));
                df.addRow(v);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_pNameTxtBoxKeyTyped

    private void igNameTxtBoxKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_igNameTxtBoxKeyTyped
        try {

            InventoryItem item = new InventoryItem();
            ResultSet rs = item.viewInvenory("*", "name like ? and type = ?", "%" + igNameTxtBox.getText() + "", "Ingredient");

            DefaultTableModel df = (DefaultTableModel) ingredientsTable.getModel();
            int rc = df.getRowCount();
            for (int i = 0; i < rc; i++) {
                df.removeRow(0);
            }

            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("item_id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("price"));
                v.add(rs.getString("quantity"));
                df.addRow(v);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_igNameTxtBoxKeyTyped

    private void saveProductBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveProductBtnActionPerformed
        DefaultTableModel df = (DefaultTableModel) productDetailsTable1.getModel();
        DefaultTableModel df2 = (DefaultTableModel) ingredientsTable.getModel();

        if (productDetailsTable1.getSelectedRowCount() != 1 || ingredientsTable.getSelectedRowCount() != 1) {
            JOptionPane.showMessageDialog(null, "Select Only one product and one ingredient at time");
        } else {

            try {

                String addIngredients = "Insert into item_product (product_id, item_id) values (?, ?)";
                int result = MyConn.save(addIngredients, pIdTxtBox.getText(), igIdTxtBox.getText());
                loadIngreProducts();

                if (result == 1) {
                    JOptionPane.showMessageDialog(null, "Successfully added");
                } else {
                    JOptionPane.showMessageDialog(null, "Failed");
                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_saveProductBtnActionPerformed

