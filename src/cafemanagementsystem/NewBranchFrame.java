    private void branchTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_branchTableMouseClicked
        DefaultTableModel df = (DefaultTableModel) branchTable.getModel();

        branchId = Integer.parseInt(df.getValueAt(branchTable.getSelectedRow(), 0).toString());
        branchIDTxtBox.setText(branchId + "");

        locationTxtBox.setText(df.getValueAt(branchTable.getSelectedRow(), 1).toString());
        emailTxtBox.setText(df.getValueAt(branchTable.getSelectedRow(), 2).toString());
        telephoneTxtBox.setText(df.getValueAt(branchTable.getSelectedRow(), 3).toString());
    }//GEN-LAST:event_branchTableMouseClicked

