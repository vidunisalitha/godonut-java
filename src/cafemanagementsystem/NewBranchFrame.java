    public void loadBranches() {

        try {

            Branch branch = new Branch();
            ResultSet rs = branch.viewBranch("*", "");

            DefaultTableModel df = (DefaultTableModel) branchTable.getModel();
            int rc = df.getRowCount();
            for (int i = 0; i < rc; i++) {
                df.removeRow(0);
            }

            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("branch_id"));
                v.add(rs.getString("location"));
                v.add(rs.getString("email"));
                v.add(rs.getString("Telephone"));
                df.addRow(v);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    public void getBranchId() {

        try {

            String id = "Select * from branch";
            ResultSet rs = MyConn.search(id);

            int bID = 0;
            while (rs.next()) {
                bID = rs.getInt("branch_id");
            }
            bID++;
            branchIDTxtBox.setText(bID + "");

            branchIDTxtBox.disable();

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

