    private void updateBranchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBranchBtnActionPerformed
        String location = locationTxtBox.getText();
        String email = emailTxtBox.getText();
        String tele = telephoneTxtBox.getText();

        if (branchId == 0) {
            JOptionPane.showMessageDialog(null, "Cannot Perform this action");
        } else {
            if (location.trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Enter the location");
                locationTxtBox.grabFocus();
            } else if (email.trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Enter the email");
                emailTxtBox.grabFocus();
            } else if (tele.trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Enter the email");
                telephoneTxtBox.grabFocus();
            } else {
                
                Branch branch = new Branch(email, location, tele);
                branch.setBranchID(branchId);
                branch.updateBranch();
                loadBranches();
                getBranchId();

            }
        }
    }//GEN-LAST:event_updateBranchBtnActionPerformed

