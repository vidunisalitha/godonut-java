    private void telephoneTxtBoxKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_telephoneTxtBoxKeyTyped
        char c = evt.getKeyChar();
        if (!(Character.isDigit(c)) || (c == KeyEvent.VK_BACK_SPACE) || (c == KeyEvent.VK_DELETE) || (c == KeyEvent.VK_PERIOD)) {
            getToolkit().beep();
            evt.consume();
        }
        
        if (telephoneTxtBox.getText().trim().length() == 10) {
            evt.consume();
        }
    }//GEN-LAST:event_telephoneTxtBoxKeyTyped

