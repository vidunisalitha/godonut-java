    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        if (expId == 0) {
            JOptionPane.showMessageDialog(null, "Please select a Expence to edit");
        } else {

            if (amountTxtBox.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please enter the amount");
            } else {

                String type = "";
                if (typeCmbBox.getSelectedItem().toString().equals("Other")) {
                    JOptionPane.showMessageDialog(null, "Please enter the Reason");
                    type = reasonTxtBox.getText();
                } else {

                    type = typeCmbBox.getSelectedItem().toString();
                    Expense exp = new Expense();
                    exp.setExpenceId(expId);
                    exp.setType(type);
                    exp.setAmount(Double.parseDouble(amountTxtBox.getText()));
                    exp.updateExpence();
                    loadExpences();

                }

            }

        }
    }//GEN-LAST:event_updateBtnActionPerformed

    private void saveExpenceBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveExpenceBtnActionPerformed
        if (amountTxtBox.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter the amount");
        } else {

            String type = "";
            if (typeCmbBox.getSelectedItem().toString().equals("Other")) {
                JOptionPane.showMessageDialog(null, "Please enter the Reason");
                type = reasonTxtBox.getText();
            } else {

                type = typeCmbBox.getSelectedItem().toString();

                java.sql.Date mkdate = java.sql.Date.valueOf(LocalDate.now());
                Time mktime = Time.valueOf(LocalTime.now());

                Expense exp = new Expense(type, mkdate, mktime, Double.parseDouble(amountTxtBox.getText()));
                exp.addExpence();
                loadExpences();

            }

        }
    }//GEN-LAST:event_saveExpenceBtnActionPerformed

    private void amountTxtBoxKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_amountTxtBoxKeyTyped
        char c = evt.getKeyChar();
        if (!(Character.isDigit(c)) || (c == KeyEvent.VK_BACK_SPACE) || (c == KeyEvent.VK_DELETE)) {
            getToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_amountTxtBoxKeyTyped

    private void expenceDetailsTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_expenceDetailsTableMouseClicked
        DefaultTableModel df = (DefaultTableModel) expenceDetailsTable.getModel();

        typeCmbBox.setSelectedItem(df.getValueAt(expenceDetailsTable.getSelectedRow(), 0).toString());
        expId = Integer.parseInt(df.getValueAt(expenceDetailsTable.getSelectedRow(), 4).toString());

        amountTxtBox.setText(df.getValueAt(expenceDetailsTable.getSelectedRow(), 3).toString());
        if (df.getValueAt(expenceDetailsTable.getSelectedRow(), 4).toString().equals("Other")) {
            reasonTxtBox.setText(df.getValueAt(expenceDetailsTable.getSelectedRow(), 0).toString());
        }
    }//GEN-LAST:event_expenceDetailsTableMouseClicked

