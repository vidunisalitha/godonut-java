    public void loadExpences() {

        try {

            Expense exp = new Expense();
            ResultSet rs = exp.viewExpence("*", "type != ? and type != ?", "Salary", "Supplier");

            DefaultTableModel df = (DefaultTableModel) expenceDetailsTable.getModel();
            int rc = df.getRowCount();
            for (int i = 0; i < rc; i++) {
                df.removeRow(0);
            }

            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("type"));
                v.add(rs.getString("made_date"));
                v.add(rs.getString("made_time"));
                v.add(rs.getString("amount"));
                v.add(rs.getString("expense_id"));
                df.addRow(v);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

