    private void saveCustomerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveCustomerBtnActionPerformed
        String fname = fNameTxtBox.getText();
        String lname = lNameTxtBox.getText();
        String contact = contactTxtBox.getText();
        String addr1 = addressTxtBox1.getText();
        String addr2 = addressTxtBox2.getText();

        if (fname.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter first name");
            fNameTxtBox.grabFocus();
        } else if (lname.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter Last name");
            lNameTxtBox.grabFocus();
        } else if (contact.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter Contatct Number");
            contactTxtBox.grabFocus();
        } else if (addr1.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter Address Line01");
            addressTxtBox1.grabFocus();
        } else if (addr2.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter Address Line02");
            addressTxtBox2.grabFocus();
        } else {

            Customer customer = new Customer(fname, lname, addr1, addr2, contact);
            customer.addCustomer();
            loadCustomers();

        }
    }//GEN-LAST:event_saveCustomerBtnActionPerformed

    private void contactTxtBoxKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_contactTxtBoxKeyTyped
        char c = evt.getKeyChar();
        if (!(Character.isDigit(c)) || (c == KeyEvent.VK_BACK_SPACE) || (c == KeyEvent.VK_DELETE)) {
            getToolkit().beep();
            evt.consume();
        }

        if (contactTxtBox.getText().trim().length() == 10) {
            evt.consume();
        }
    }//GEN-LAST:event_contactTxtBoxKeyTyped

    private void customerInfoTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_customerInfoTableMouseClicked
        DefaultTableModel df = (DefaultTableModel) customerInfoTable.getModel();

        cusid = Integer.parseInt(df.getValueAt(customerInfoTable.getSelectedRow(), 0).toString());
        String cusName = df.getValueAt(customerInfoTable.getSelectedRow(), 1).toString();
        String splitArr[] = cusName.split(" ");
        fNameTxtBox.setText(splitArr[0]);
        lNameTxtBox.setText(splitArr[1]);
        contactTxtBox.setText(df.getValueAt(customerInfoTable.getSelectedRow(), 3).toString());
        String addr = df.getValueAt(customerInfoTable.getSelectedRow(), 2).toString();
        String addrSplit[] = addr.split(",");
        addressTxtBox1.setText(addrSplit[0]);
        addressTxtBox2.setText(addrSplit[1]);
    }//GEN-LAST:event_customerInfoTableMouseClicked

