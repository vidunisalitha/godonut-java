    public void loadCustomers() {

        try {

            Customer cus = new Customer();
            ResultSet rs = cus.viewCustomer("*", "");

            DefaultTableModel df = (DefaultTableModel) customerInfoTable.getModel();
            int rc = df.getRowCount();
            for (int i = 0; i < rc; i++) {
                df.removeRow(0);
            }

            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("cus_id"));
                v.add(rs.getString("fname") + " " + rs.getString("lname"));
                v.add(rs.getString("address_line1") + "," + rs.getString("address_line2"));
                v.add(rs.getString("tele"));
                df.addRow(v);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

