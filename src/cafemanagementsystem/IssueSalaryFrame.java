    public void loadUsers() {

        try {

            User user = new User();

            ResultSet rs = user.viewUser("*", "");

            DefaultTableModel df = (DefaultTableModel) usersTable.getModel();
            int rc = df.getRowCount();
            for (int i = 0; i < rc; i++) {
                df.removeRow(0);
            }

            while (rs.next()) {

                Vector v = new Vector();
                String userId = rs.getString("user_id");
                v.add(userId);
                v.add(rs.getString("fname"));
                v.add(rs.getString("role"));
                v.add(rs.getString("salary"));
                df.addRow(v);

            }
            rs.close();

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    public void loadSal() {

        try {
            
            Salary sal = new Salary();
            ResultSet rs = sal.viewSalary("*", "Inner join expense as e on e.expense_id = s.expense_id inner join user as u on u.user_id = s.user_id", "");
            
            DefaultTableModel df = (DefaultTableModel) salaryTable1.getModel();
            int rc = df.getRowCount();
            for (int i = 0; i < rc; i++) {
                df.removeRow(0);
            }
            
            while (rs.next()) {                
                
                Vector v = new Vector();
                v.add(rs.getString("s.user_id"));
                v.add(rs.getString("e.made_date"));
                v.add(rs.getString("e.made_time"));
                v.add(rs.getString("u.salary"));
                v.add(rs.getString("s.bonus"));
                v.add(rs.getString("s.deductions"));
                v.add(rs.getString("e.amount"));
                df.addRow(v);
                
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

