    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        if (bonusTxtBox.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "If there isn't bonus, please enter 0");
        } else if (dedTxtBox.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "If there isn't a deduction, please enter 0");
        } else if (uIdTxtBox.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please select a User to give salary");
        } else {

            java.sql.Date mkdate = java.sql.Date.valueOf(LocalDate.now());
            Time mktime = Time.valueOf(LocalTime.now());

            double totAmount = Double.parseDouble(salaryTxtBox.getText()) + Double.parseDouble(bonusTxtBox.getText()) - Double.parseDouble(dedTxtBox.getText());

            Expense exp = new Expense("Salary", mkdate, mktime, totAmount);

            try {
                exp.addExpence();

//                String getNewId = "SELECT LAST_INSERT_ID() AS id";
                String query = "SELECT expense_id FROM expense WHERE type = ? AND made_date = ? AND made_time = ? AND amount = ?";
                ResultSet rs = MyConn.search(query, "Salary", mkdate, mktime, totAmount);
                int newExpenseId = -1;
                if (rs.next()) {
                    newExpenseId = rs.getInt("expense_id");
                }

                if (newExpenseId == -1) {
                    JOptionPane.showMessageDialog(null, "Error retrieving the new expense ID.");
                    return;
                }

                System.out.println(newExpenseId);

                Salary sal = new Salary(newExpenseId, uIdTxtBox.getText(), Double.parseDouble(bonusTxtBox.getText()), Double.parseDouble(dedTxtBox.getText()));
                sal.addSalary();

            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_saveBtnActionPerformed

    private void usersTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_usersTableMouseClicked
        usersTable.setEnabled(true);
        DefaultTableModel df = (DefaultTableModel) usersTable.getModel();
        int selectedRow = usersTable.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Please select a user from the table.");
        } else {
            uIdTxtBox.setText(df.getValueAt(selectedRow, 0).toString());
            uNameTxtBox.setText(df.getValueAt(selectedRow, 1).toString());
            salaryTxtBox.setText(df.getValueAt(selectedRow, 3).toString());
        }

    }//GEN-LAST:event_usersTableMouseClicked

    private void bonusTxtBoxKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_bonusTxtBoxKeyTyped
        char c = evt.getKeyChar();
        if (!(Character.isDigit(c)) || (c == KeyEvent.VK_BACK_SPACE) || (c == KeyEvent.VK_DELETE)) {
            getToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_bonusTxtBoxKeyTyped

