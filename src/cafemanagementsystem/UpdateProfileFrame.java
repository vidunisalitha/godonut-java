    public void loadUsers() {

        try {

            User user = new User();

            ResultSet rs = user.viewUser("*", "user_id = ?", uid);

            String id = "";
            String fname = "";
            String lname = "";
            String email = "";
            String te1e1 = "";
            String tele2 = "";
            String addr1 = "";
            String addr2 = "";
            String nic = "";

            while (rs.next()) {

                id = (rs.getString("user_id"));

                fname = (rs.getString("fname"));
                lname = (rs.getString("lname"));
                email = (rs.getString("email"));

                String contactQuery = "SELECT tele FROM User_contact WHERE user_id = ?";
                ResultSet rsContacts = MyConn.search(contactQuery, uid);

                int contactIndex = 0;
                while (rsContacts.next()) {
                    if (contactIndex == 0) {
                        te1e1 = rsContacts.getString("tele");
                    } else if (contactIndex == 1) {
                        tele2 = rsContacts.getString("tele");
                    }
                    contactIndex++;
                }
                rsContacts.close();
                addr1 = (rs.getString("address_line1"));
                addr2 = (rs.getString("address_line2"));
                nic = (rs.getString("nic"));

            }

            UserIdTxtBox.setText(id);
            fNameTxtBox.setText(fname);
            lNameTxtBox.setText(lname);
            emailTxtBox.setText(email);
            contactTxtBox02.setText(tele2);
            contactTxtBox01.setText(te1e1);
            addrLineTxtBox1.setText(addr1);
            addrLineTxtBox1.setText(addr2);
            nicTxtBox.setText(nic);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

