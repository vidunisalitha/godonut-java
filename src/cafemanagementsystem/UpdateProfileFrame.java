    private void savePwdBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savePwdBtnActionPerformed
        if (newPasswordTxtBox.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter new Password");
        } else if (otp == 0) {
            JOptionPane.showMessageDialog(null, "Please get the OTP");
        }else {
        
            try {
                
                String upPassword = "Update user set password = ? where user_id = ?";
                int result = MyConn.save(upPassword, newPasswordTxtBox.getText(), uid);
                
                if (result == 1) {
                    JOptionPane.showMessageDialog(null, "SuccessFull");
                } else {
                    JOptionPane.showMessageDialog(null, "Failed");
                }
                
            } catch (Exception e) {
                e.printStackTrace();
            }
        
        }
    }//GEN-LAST:event_savePwdBtnActionPerformed

