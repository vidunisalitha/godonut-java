    private void contactTxtBox01KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_contactTxtBox01KeyTyped
        char c = evt.getKeyChar();
        if (!(Character.isDigit(c)) || (c == KeyEvent.VK_BACK_SPACE) || (c == KeyEvent.VK_DELETE)) {
            getToolkit().beep();
            evt.consume();
        }

        if (contactTxtBox01.getText().trim().length() == 10) {
            evt.consume();
        }
    }//GEN-LAST:event_contactTxtBox01KeyTyped

