    private void priceTxtBoxKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_priceTxtBoxKeyTyped
        char c = evt.getKeyChar();
        if (!(Character.isDigit(c)) || (c == KeyEvent.VK_BACK_SPACE) || (c == KeyEvent.VK_DELETE)) {
            getToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_priceTxtBoxKeyTyped

