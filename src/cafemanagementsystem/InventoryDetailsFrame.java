    private void updateInventoryBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateInventoryBtnActionPerformed
        String itmName = inventoryNameTxtBox.getText();
        String itmType = itemTypeCmbBox.getSelectedItem().toString();
        String price = priceTxtBox.getText();
        int qty = qtySpinner.getValue();

        String unit = "";
        if (pcsRBtn.isSelected()) {
            unit = "pcs";
        } else if (kgRBtn.isSelected()) {
            unit = "kg";
        } else if (mlRBtn.isSelected()) {
            unit = "ml";
        } else if (lRBtn.isSelected()) {
            unit = "L";
        }

        if (itmId == 0) {
            JOptionPane.showMessageDialog(null, "Cannot perform this action");
        } else {

            if (itmName.trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Enter item name");
                inventoryNameTxtBox.grabFocus();
            } else if (price.trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Enter item name");
                priceTxtBox.grabFocus();
            } else if (qty == 0) {
                JOptionPane.showMessageDialog(null, "Select a quantity");
            } else if (buttonGroup1.getSelection() == null) {
                JOptionPane.showMessageDialog(null, "Select a Unit");
            } else {

                JFrame f = new JFrame();
                if (JOptionPane.showConfirmDialog(f, "Are all the details correct") == JOptionPane.YES_NO_OPTION) {

                    InventoryItem item = new InventoryItem(itmName, itmType, Double.parseDouble(price), qty, unit);
                    item.setItemId(itmId);
                    item.updateItem();
                    loadItem();
                    setId();

                }

            }

        }
    }//GEN-LAST:event_updateInventoryBtnActionPerformed

