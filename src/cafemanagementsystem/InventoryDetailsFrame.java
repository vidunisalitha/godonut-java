    private void inventoryTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_inventoryTableMouseClicked
        DefaultTableModel df = (DefaultTableModel) inventoryTable.getModel();

        itmId = Integer.parseInt(df.getValueAt(inventoryTable.getSelectedRow(), 0).toString());
        inventoryNameTxtBox.setText(df.getValueAt(inventoryTable.getSelectedRow(), 1).toString());
        itemTypeCmbBox.setSelectedItem(df.getValueAt(inventoryTable.getSelectedRow(), 2).toString());
        priceTxtBox.setText(df.getValueAt(inventoryTable.getSelectedRow(), 3).toString());
        qtySpinner.setValue(Integer.parseInt(df.getValueAt(inventoryTable.getSelectedRow(), 4).toString()));

        String unit = df.getValueAt(inventoryTable.getSelectedRow(), 5).toString();
        if (unit.equals("pcs")) {
            pcsRBtn.setSelected(true);
        } else if (unit.equals("kg")) {
            kgRBtn.setSelected(true);
        } else if (unit.equals("ml")) {
            mlRBtn.setSelected(true);
        } else if (unit.equals("L")) {
            lRBtn.setSelected(true);
        }
    }//GEN-LAST:event_inventoryTableMouseClicked

