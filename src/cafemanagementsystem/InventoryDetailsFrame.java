    public void loadItem() {

        try {

            InventoryItem item = new InventoryItem();
            ResultSet rs = item.viewInvenory("*", "");

            DefaultTableModel df = (DefaultTableModel) inventoryTable.getModel();
            int rc = df.getRowCount();
            for (int i = 0; i < rc; i++) {
                df.removeRow(0);
            }

            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("item_id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("type"));
                v.add(rs.getString("price"));
                v.add(rs.getString("quantity"));
                v.add(rs.getString("unit"));
                df.addRow(v);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    public void setId() {

        DefaultTableModel df = (DefaultTableModel) inventoryTable.getModel();
        int rc = df.getRowCount();

        if (rc > 0) {

            int lastRowIndex = rc - 1;
            int cc = df.getColumnCount();
            for (int ci = 0; ci < cc; ci++) {
                Object val = df.getValueAt(lastRowIndex, ci);
            }

            int id = Integer.parseInt(df.getValueAt(lastRowIndex, 0).toString());
            id += 1;
            inventoryIDTxtBox.setText(id + "");
            inventoryIDTxtBox.disable();

        }

    }

