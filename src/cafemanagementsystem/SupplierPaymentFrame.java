    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        if (suppIDtxtbox.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Please select a supplier");
        } else if (amountTxtBox.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Please Enetr an Amount");
        }else if (discountTxtBox.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Enter discout as 0 if there isn't any");
        }else{
        
            String item = itemsCmbBox.getSelectedItem().toString();
            String spitItem[] = item.split("-");
            int itmID = Integer.parseInt(spitItem[0]);
            
            double tot = Double.parseDouble(amountTxtBox.getText()) - Double.parseDouble(discountTxtBox.getText());
        
            java.sql.Date mkdate = java.sql.Date.valueOf(LocalDate.now());
            Time mktime = Time.valueOf(LocalTime.now());
            
            Expense exp = new Expense("Supplier", mkdate, mktime, tot);

            try {
                exp.addExpence();

//                String getNewId = "SELECT LAST_INSERT_ID() AS id";
                String query = "SELECT expense_id FROM expense WHERE type = ? AND made_date = ? AND made_time = ? AND amount = ?";
                ResultSet rs = MyConn.search(query, "Supplier", mkdate, mktime, tot);
                int newExpenseId = -1;
                if (rs.next()) {
                    newExpenseId = rs.getInt("expense_id");
                }

                if (newExpenseId == -1) {
                    JOptionPane.showMessageDialog(null, "Error retrieving the new expense ID.");
                    return;
                }

                System.out.println(newExpenseId);

                SupplierPurchase sap = new SupplierPurchase(newExpenseId, itmID, Integer.parseInt(suppIDtxtbox.getText()), invNumberTxtBox.getText(), Double.parseDouble(discountTxtBox.getText()));
                sap.addSupplierPurchase();

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
       
    }//GEN-LAST:event_saveBtnActionPerformed

