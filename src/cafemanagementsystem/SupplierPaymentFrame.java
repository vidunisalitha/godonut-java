    public void loadSuppliers() {

        try {

            Supplier sup = new Supplier();
            ResultSet rs = sup.viewSupplier("*", "");

            DefaultTableModel df = (DefaultTableModel) supplierTable.getModel();
            int rc = df.getRowCount();
            for (int i = 0; i < rc; i++) {
                df.removeRow(0);
            }

            while (rs.next()) {

                Vector v = new Vector();
                String supId = rs.getString("supp_id");
                v.add(supId);

                v.add(rs.getString("fname"));
                v.add(rs.getString("email"));

                String contactQuery = "SELECT tele FROM supplier_contact WHERE supp_id = ?";
                ResultSet rsContacts = MyConn.search(contactQuery, supId);

                String tele1 = "";
                String tele2 = "";

                int contactIndex = 0;
                while (rsContacts.next()) {
                    if (contactIndex == 0) {
                        tele1 = rsContacts.getString("tele");
                    } else if (contactIndex == 1) {
                        tele2 = rsContacts.getString("tele");
                    }
                    contactIndex++;
                }

                rsContacts.close();

                v.add(tele1);
                df.addRow(v);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

