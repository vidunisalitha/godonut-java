    private void spNameTxtBoxKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_spNameTxtBoxKeyTyped
        //loadProducts("name like ?", "%" + pNameTxtBox.getText() + "%");

        try {

            Product pdct = new Product();
            ResultSet rs = pdct.viewProduct("*", "name like ?", "%" + spNameTxtBox.getText() + "%");
            DefaultTableModel df = (DefaultTableModel) productDetailsTable.getModel();
            int rc = df.getRowCount();
            for (int i = 0; i < rc; i++) {
                df.removeRow(0);
            }

            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("product_id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("Description"));
                v.add(rs.getString("price"));
                v.add(rs.getString("Category"));
                boolean avail = rs.getBoolean("availability");
                if (avail) {
                    v.add("Available");
                } else {
                    v.add("Not-Available");
                }
                df.addRow(v);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_spNameTxtBoxKeyTyped

