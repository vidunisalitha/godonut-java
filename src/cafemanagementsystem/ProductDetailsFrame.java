    private void deleteProductBtntBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteProductBtntBtnActionPerformed
        DefaultTableModel df = (DefaultTableModel) productDetailsTable.getModel();

        if (productDetailsTable.getSelectedRowCount() == 1) {

            JFrame f = new JFrame();
            if (JOptionPane.showConfirmDialog(f, "Do you want to delete this product") == JOptionPane.YES_NO_OPTION) {
                Product pdct = new Product();
                pdct.setProductID(df.getValueAt(productDetailsTable.getSelectedRow(), 0).toString());
                pdct.removeProduct();
                loadProducts("");
                incrementPId();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select a Product to delete");
        }
    }//GEN-LAST:event_deleteProductBtntBtnActionPerformed

