    public void incrementPId() {

        pIDTxtBox.setText("PDCT-1000");
        try {

            String getID = "Select * from product";
            ResultSet rs = MyConn.search(getID);
            while (rs.next()) {

                String id = rs.getString("product_id");
                String splitID[] = id.split("-");
                int lastVal = Integer.parseInt(splitID[1]);
                lastVal++;
                pIDTxtBox.setText("PDCT-" + lastVal);

            }

            pIDTxtBox.disable();
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    public void loadProducts(String condition, Object... values) {

        try {

            Product pdct = new Product();
            ResultSet rs = pdct.viewProduct("*", condition, values);

            DefaultTableModel df = (DefaultTableModel) productDetailsTable.getModel();
            int rc = df.getRowCount();
            for (int i = 0; i < rc; i++) {
                df.removeRow(0);
            }

            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("product_id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("Description"));
                v.add(rs.getString("price"));
                v.add(rs.getString("Category"));
                boolean avail = rs.getBoolean("availability");
                if (avail) {
                    v.add("Yes");
                } else {
                    v.add("No");
                }

                df.addRow(v);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

